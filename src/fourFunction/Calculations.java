package fourFunction;

/**
 * The four function calculator math goes here
 * @author ciara
 *
 */
public class Calculations {

	/**
	 * The addition function. Returns the result of adding two numbers together.
	 * @param a
	 * @param b
	 * @return
	 */
	double addNumbers(double a, double b) {
		return a+b;
	}
	
	/**
	 * The subtraction function. Returns the result of subtracting the second number from the first.
	 * @param a
	 * @param b
	 * @return
	 */
	double subtractNumbers(double a, double b) {
		return a-b;
	}
	
	/**
	 * The division function. Returns the result of dividing the first number by the second number.
	 * @param a
	 * @param b
	 * @return
	 */
	double divideNumbers(double a, double b) {
		return a/b;
	}
	
	/**
	 * The multiplication function. Returns the result of multiplying both numbers together.
	 * @param a
	 * @param b
	 * @return
	 */
	double multiplyNumbers(double a, double b) {
		return a*b;
	}
	
}//class
